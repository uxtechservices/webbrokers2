<script setup lang="ts">
import { ref } from 'vue'
import PageBreadcrumb from '@/components/common/PageBreadcrumb.vue'
import AdminLayout from '@/components/layout/AdminLayout.vue'
import axios from "axios";
import event from "@/libs/event";
import Alert from "@/components/ui/Alert.vue";
import { BASE_SUBAGENTE, type SubagenteTS } from '@/interfaces/SubagentesTS';
import instance from '@/libs/axios';
import { getToken } from '@/libs/authentication';

const agentSchema = ref<SubagenteTS>(BASE_SUBAGENTE);
const currentPageTitle = ref('Crear subagente')
const errorAlert = ref(false);
const errorMessage = ref("");



const createAgent = async () => {
  // Implement save profile logic here
  // Handle form submission
  try {
    delete agentSchema.value.id;
    errorAlert.value = false;
    const { data } = await instance.post('/agents', agentSchema.value, {headers: {
      Authorization: 'Bearer ' + getToken()
    }});

    if (data) {
      window.location.href="/subagentes"
      event.emit('reload-agent-table');
    }
  } catch (error) {
    if (axios.isAxiosError(error)) {
      errorAlert.value = true;
      errorMessage.value = error.response?.data.message;
    }
  }
}
</script>
